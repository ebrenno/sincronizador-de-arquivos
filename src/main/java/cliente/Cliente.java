    public void despertar(){
        new Thread(new Runnable() {
            
            @Override
            public void run() {
                while(ligada){
                    // loop do contador
                    do {
                        try {
                            Thread.sleep(1000);
                            //caso o serviço seja desligado na contagem
                            if (!ligada) {
                                return;
                            }
                            jLabelTempo.setText( String.valueOf( --tempo ) );

                        } catch (InterruptedException ex) {
                            ex.printStackTrace();
                        }

                    } while (tempo > 0);
                    
                    try{
                        //inicia a thread caso ela não esteja ligada
                        if(!t1.isAlive()){
                            t1 = new Thread(tcu);
                            t1.start();
                        }
                    }catch(NullPointerException e){
                        //captura a excecao da condicional acima e inicia a thread                      
                        t1 = new Thread(tcu);
                        t1.start();
                    }
                    
                    try{
                        if(!t2.isAlive()){
                            t2 = new Thread(tcd);
                            t2.start();
                        }                    
                    }catch(NullPointerException e){                       
                        t2 = new Thread(tcd);
                        t2.start(); 
                    }  
                    
                    jLabelTempo.setText( "---" );
                    
                    do{
                        try {
                            Thread.sleep( 5000 );
                        } catch (InterruptedException ex) {
                            ex.printStackTrace();
                        }
                    }while(t1.isAlive() && t2.isAlive());
                    //reseta o tempo
                    tempo = Integer.parseInt( jTextTempo.getText() ) ;
   
                }
            }
        }).start();
    }
        
