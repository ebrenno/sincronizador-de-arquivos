    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String serverIp = jTextIp.getText();
        int portaUpload = 2000;
        int portaDownload = 4000;
        tempo = Integer.parseInt( jTextTempo.getText() );
        jButton1.setEnabled(true);

        try {
            tcu = new ThreadClienteUpload(diretorio , portaUpload, serverIp);
            tcd = new ThreadClienteDownload(diretorio , portaDownload, serverIp);
        } catch (UnsupportedEncodingException e) {
            throw new RuntimeException(e);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(jButton1.getText().equals("ligar")){
            camposEditaveis(false);

            ligada = true;
            if(tempo == 0)tempo = Integer.parseInt( jTextTempo.getText() ) ;
            despertar();

            jButton1.setText("desligar");
            return;
        }
        if(jButton1.getText().equals("desligar")){
            //desligar thread//
            camposEditaveis(true);
            ligada = false;
            jButton1.setText("ligar");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

